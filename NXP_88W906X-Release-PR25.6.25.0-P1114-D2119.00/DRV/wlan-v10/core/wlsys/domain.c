static int isInChannelList(UINT8 channel, UINT8 band, UINT8 * ChannelList)
{
	int i;

	if (band == FREQ_BAND_5GHZ) {
		for (i = 14; i < IEEE_80211_MAX_NUMBER_OF_CHANNELS; i++) {
			if (channel != 0 && channel == ChannelList[i])
				return 1;
		}
	} else {
		for (i = 0; i < 14; i++) {
			if (channel != 0 && channel == ChannelList[i])
				return 1;
		}
	}
	return 0;
}

