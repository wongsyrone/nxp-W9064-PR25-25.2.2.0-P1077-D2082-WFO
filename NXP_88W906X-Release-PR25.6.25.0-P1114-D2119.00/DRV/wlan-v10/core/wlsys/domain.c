UINT8 FindFallbackChannel(UINT8 fallbackCnt, UINT8 * FallbackChannelList, UINT8 BW)
{
	UINT8 j, fallbackchannel = 0;
	UINT8 m = 0xff, randInd = 0;

	randInd = jiffies % fallbackCnt;

	for (j = 0; j < fallbackCnt; j++) {
		if ((j + randInd) < fallbackCnt) {
			fallbackchannel = FallbackChannelList[j + randInd];
			if (BW == CH_80_MHz_WIDTH) {
				if (Is80MzChannelInFallBack(FallbackChannelList, fallbackchannel, fallbackCnt)) {
					break;
				}

			}
			if (BW == CH_40_MHz_WIDTH) {
				if (Is40MzChannel(FallbackChannelList, fallbackchannel, fallbackCnt)) {
					break;
				}
			}

		}
	}
	if (j == fallbackCnt) {
		for (m = 0; m < randInd; m++) {
			fallbackchannel = FallbackChannelList[m];
			if (BW == CH_80_MHz_WIDTH) {
				if (Is80MzChannelInFallBack(FallbackChannelList, fallbackchannel, fallbackCnt)) {
					break;
				}
			}
			if (BW == CH_40_MHz_WIDTH) {
				if (Is40MzChannel(FallbackChannelList, fallbackchannel, fallbackCnt)) {
					break;
				}
			}

		}
	}

	if (m == randInd) {

		return 0;
	}
	return fallbackchannel;
}

